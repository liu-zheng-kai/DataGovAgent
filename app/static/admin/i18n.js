  function normalizeLocale(locale) {
    const lower = String(locale || "").toLowerCase();
    if (lower.startsWith("zh")) return "zh";
    if (lower.startsWith("ja")) return "ja";
    return "en";
  }

  function getLocale() {
    const stored = globalThis.localStorage?.getItem(STORAGE_KEY);
    const fallback = normalizeLocale(globalThis.navigator?.language || "en");
    return supportedLocales.includes(stored) ? stored : fallback;
  }

  function translateText(text) {
    const raw = String(text ?? "");
    const trimmed = raw.trim();
    if (!trimmed) return raw;

    const locale = getLocale();
    const translated = messages[locale]?.[trimmed];
    if (translated) {
      return raw.replace(trimmed, translated);
    }

    if (trimmed.startsWith("Scene: ")) {
      return raw.replace(trimmed, `${t("Scene")}: ${trimmed.slice(7)}`);
    }
    if (trimmed.startsWith("Template: ")) {
      return raw.replace(trimmed, `${t("Template")}: ${trimmed.slice(10)}`);
    }
    if (trimmed.startsWith("System Status: ")) {
      return raw.replace(trimmed, `${t("System Status")}: ${trimmed.slice(15)}`);
    }
    if (trimmed.startsWith("Tool Detail #")) {
      return raw.replace(trimmed, `${t("Detail")} #${trimmed.slice(13)}`);
    }

    const msgMatch = trimmed.match(/^(\d+)\s+msgs\s+·\s+(.+)$/);
    if (msgMatch) {
      return raw.replace(trimmed, `${msgMatch[1]} ${t("msgs")} · ${msgMatch[2]}`);
    }

    return raw;
  }

  function t(key, vars = {}) {
    let value = messages[getLocale()]?.[key] || key;
    Object.entries(vars).forEach(([name, replacement]) => {
      value = value.replaceAll(`{${name}}`, replacement);
    });
    return value;
  }

  function applyTranslations(root = document) {
    if (!root) return;
    document.documentElement.lang = getLocale();
    document.title = t("DataGovAgent Admin Console");

    const selectors = [
      "button",
      "label",
      "h1",
      "h2",
      "h3",
      "p",
      "span",
      "summary",
      "th",
      "option",
      ".empty",
      ".session-chip",
      ".status",
      ".title",
      ".meta"
    ];

    root.querySelectorAll(selectors.join(",")).forEach((el) => {
      if (el.closest("pre, code")) return;
      if (!el.children.length) {
        const next = translateText(el.textContent);
        if (next !== el.textContent) el.textContent = next;
      }
    });

    root.querySelectorAll("input[placeholder], textarea[placeholder]").forEach((el) => {
      const value = el.getAttribute("placeholder");
      const next = translateText(value);
      if (next !== value) el.setAttribute("placeholder", next);
    });
  }

  function setLocale(locale) {
    const next = normalizeLocale(locale);
    globalThis.localStorage?.setItem(STORAGE_KEY, next);
    document.documentElement.lang = next;
    listeners.forEach((listener) => listener(next));
  }

  function onLocaleChange(listener) {
    listeners.add(listener);
    return () => listeners.delete(listener);
  }

  window.AdminI18n = {
    applyTranslations,
    getLocale,
    onLocaleChange,
    setLocale,
    supportedLocales,
    t,
    translateText
  };
